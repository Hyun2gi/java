//예제6 : 대입 연산자와 증감 연산자
package ex6;

public class in {

	public static void main(String[] args) {
		int opr = 0;
		opr += 3;
		System.out.println(opr++);
		System.out.println(opr);
		System.out.println(++opr);
		System.out.println(opr);
		System.out.println(opr--);
		System.out.println(opr);
		System.out.println(--opr);
		System.out.println(opr);
	}

}
